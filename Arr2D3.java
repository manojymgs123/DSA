
    import java.util.Scanner;

    public class Arr2D3 {
        public static void main(String[] args) {
            try (Scanner sc = new Scanner(System.in)) {
                int n = sc.nextInt();
                int[][] arr = new int[n][n];
                for (int i = 0; i < arr.length ; i++) {
                    for(int j=0; j<arr[0].length  ; j++){
                    arr[i][j] = sc.nextInt();
   
                }
   
      }
            } 
    }
}
